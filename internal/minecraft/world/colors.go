func mustParseColor(s string) (c color.RGBA) {
	c.A = 0xff
	var err error

	switch len(s) {
	case 9:
		_, err = fmt.Sscanf(s, "#%02x%02x%02x%02x", &c.R, &c.G, &c.B, &c.A)
	case 7:
		_, err = fmt.Sscanf(s, "#%02x%02x%02x", &c.R, &c.G, &c.B)
	case 5:
		_, err = fmt.Sscanf(s, "#%1x%1x%1x%1x", &c.R, &c.G, &c.B, &c.A)
		c.A *= 17
		c.R *= 17
		c.G *= 17
		c.B *= 17
	case 4:
		_, err = fmt.Sscanf(s, "#%1x%1x%1x", &c.R, &c.G, &c.B)
		c.R *= 17
		c.G *= 17
		c.B *= 17
	default:
		err = errors.New("invalid length.")
	}

	if err != nil {
		log.Fatal().Err(err).Str("color", s).Msg("cannot parse string")
	}

	return
}
